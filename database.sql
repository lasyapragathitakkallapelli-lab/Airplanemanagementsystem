-- ── ADMIN VIEW ──────────────────────────────────────────────────

CREATE OR REPLACE VIEW v_booking_summary AS
SELECT
    b.booking_id,
    u.username,
    u.email,
    f.flight_name,
    f.source,
    f.destination,
    b.travel_date,
    b.total_amount,
    p.payment_method,
    p.payment_status,
    b.booked_at
FROM bookings b
JOIN users    u ON b.user_id   = u.user_id
JOIN flights  f ON b.flight_id = f.flight_id
LEFT JOIN payments p ON b.booking_id = p.booking_id;

