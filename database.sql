CREATE DATABASE IF NOT EXISTS airline_portals
    CHARACTER SET utf8mb4
    COLLATE utf8mb4_unicode_ci;
USE airline_portals;

-- ── TABLES ──────────────────────────────────────────────────────

CREATE TABLE users (
    user_id    INT          AUTO_INCREMENT PRIMARY KEY,
    username   VARCHAR(80)  NOT NULL UNIQUE,
    email      VARCHAR(120) NOT NULL UNIQUE,
    password   VARCHAR(255) NOT NULL,
    created_at TIMESTAMP    DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE  airports (
    airport_id   INT          AUTO_INCREMENT PRIMARY KEY,
    city         VARCHAR(100) NOT NULL UNIQUE,
    airport_name VARCHAR(200) NOT NULL,
    iata_code    CHAR(3)      NOT NULL UNIQUE
);

CREATE TABLE  flights (
    flight_id    INT           AUTO_INCREMENT PRIMARY KEY,
    flight_name  VARCHAR(80)   NOT NULL,
    source       VARCHAR(100)  NOT NULL,
    destination  VARCHAR(100)  NOT NULL,
    dep_time     TIME          NOT NULL,
    arr_time     TIME          NOT NULL,
    price        DECIMAL(10,2) NOT NULL,
    total_seats  INT           NOT NULL DEFAULT 120,
    is_active    TINYINT(1)    NOT NULL DEFAULT 1,
    FOREIGN KEY (source)      REFERENCES airports(city) ON UPDATE CASCADE ON DELETE RESTRICT,
    FOREIGN KEY (destination) REFERENCES airports(city) ON UPDATE CASCADE ON DELETE RESTRICT
);

CREATE TABLE IF NOT EXISTS bookings (
    booking_id   INT           AUTO_INCREMENT PRIMARY KEY,
    user_id      INT           NOT NULL,
    flight_id    INT           NOT NULL,
    travel_date  DATE          NOT NULL,
    total_amount DECIMAL(10,2) NOT NULL,
    booked_at    TIMESTAMP     DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (user_id)   REFERENCES users(user_id)     ON DELETE CASCADE,
    FOREIGN KEY (flight_id) REFERENCES flights(flight_id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS passengers (
    passenger_id INT          AUTO_INCREMENT PRIMARY KEY,
    booking_id   INT          NOT NULL,
    name         VARCHAR(150) NOT NULL,
    age          TINYINT      NOT NULL,
    gender       ENUM('Male','Female','Other') NOT NULL,
    seat_no      VARCHAR(4)   NOT NULL,
    FOREIGN KEY (booking_id) REFERENCES bookings(booking_id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS payments (
    payment_id     INT           AUTO_INCREMENT PRIMARY KEY,
    booking_id     INT           NOT NULL UNIQUE,
    payment_method VARCHAR(50)   NOT NULL,
    amount         DECIMAL(10,2) NOT NULL,
    payment_status VARCHAR(20)   NOT NULL DEFAULT 'Pending',
    paid_at        TIMESTAMP     DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (booking_id) REFERENCES bookings(booking_id) ON DELETE CASCADE
);

